package br.com.hms.control;

public class Leito {
    private int leit_codi, ala_codi, quar_codi;
    private String leit_desc;

    public int getLeit_codi() {
        return leit_codi;
    }

    public void setLeit_codi(int leit_codi) {
        this.leit_codi = leit_codi;
    }

    public int getAla_codi() {
        return ala_codi;
    }

    public void setAla_codi(int ala_codi) {
        this.ala_codi = ala_codi;
    }

    public int getQuar_codi() {
        return quar_codi;
    }

    public void setQuar_codi(int quar_codi) {
        this.quar_codi = quar_codi;
    }

    public String getLeit_desc() {
        return leit_desc;
    }

    public void setLeit_desc(String leit_desc) {
        this.leit_desc = leit_desc;
    }
    
    
}
