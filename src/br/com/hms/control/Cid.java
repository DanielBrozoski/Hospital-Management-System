package br.com.hms.control;

public class Cid {
    private int cid_codi;
    private String cid_nome, cid_desc;

    public int getCid_codi() {
        return cid_codi;
    }

    public void setCid_codi(int cid_codi) {
        this.cid_codi = cid_codi;
    }

    public String getCid_nome() {
        return cid_nome;
    }

    public void setCid_nome(String cid_nome) {
        this.cid_nome = cid_nome;
    }

    public String getCid_desc() {
        return cid_desc;
    }

    public void setCid_desc(String cid_desc) {
        this.cid_desc = cid_desc;
    }
    
    
}
