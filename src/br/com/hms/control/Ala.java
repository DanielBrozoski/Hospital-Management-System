
package br.com.hms.control;

public class Ala {
    private int ala_codi, ala_nqua;
    private String ala_desc, ala_tipo;

    public int getAla_codi() {
        return ala_codi;
    }

    public void setAla_codi(int ala_codi) {
        this.ala_codi = ala_codi;
    }

    public int getAla_nqua() {
        return ala_nqua;
    }

    public void setAla_nqua(int ala_nqua) {
        this.ala_nqua = ala_nqua;
    }

    public String getAla_desc() {
        return ala_desc;
    }

    public void setAla_desc(String ala_desc) {
        this.ala_desc = ala_desc;
    }

    public String getAla_tipo() {
        return ala_tipo;
    }

    public void setAla_tipo(String ala_tipo) {
        this.ala_tipo = ala_tipo;
    }
    
    
}
