package br.com.hms.control;

public class Medicamento {
    private int medi_codi, medi_unid;
    private String medi_nome, medi_desc;

    public int getMedi_codi() {
        return medi_codi;
    }

    public void setMedi_codi(int medi_codi) {
        this.medi_codi = medi_codi;
    }

    public int getMedi_unid() {
        return medi_unid;
    }

    public void setMedi_unid(int medi_unid) {
        this.medi_unid = medi_unid;
    }

    public String getMedi_nome() {
        return medi_nome;
    }

    public void setMedi_nome(String medi_nome) {
        this.medi_nome = medi_nome;
    }

    public String getMedi_desc() {
        return medi_desc;
    }

    public void setMedi_desc(String medi_desc) {
        this.medi_desc = medi_desc;
    }
    
    
}
