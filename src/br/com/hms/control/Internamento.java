package br.com.hms.control;

import java.util.Date;

public class Internamento {
    private int inte_codi, inte_hora, pron_codi, pess_codi, leit_codi;
    private String inte_desc;
    private Date inte_data;

    public int getInte_codi() {
        return inte_codi;
    }

    public void setInte_codi(int inte_codi) {
        this.inte_codi = inte_codi;
    }

    public int getInte_hora() {
        return inte_hora;
    }

    public void setInte_hora(int inte_hora) {
        this.inte_hora = inte_hora;
    }

    public int getPron_codi() {
        return pron_codi;
    }

    public void setPron_codi(int pron_codi) {
        this.pron_codi = pron_codi;
    }

    public int getPess_codi() {
        return pess_codi;
    }

    public void setPess_codi(int pess_codi) {
        this.pess_codi = pess_codi;
    }

    public int getLeit_codi() {
        return leit_codi;
    }

    public void setLeit_codi(int leit_codi) {
        this.leit_codi = leit_codi;
    }

    public String getInte_desc() {
        return inte_desc;
    }

    public void setInte_desc(String inte_desc) {
        this.inte_desc = inte_desc;
    }

    public Date getInte_data() {
        return inte_data;
    }

    public void setInte_data(Date inte_data) {
        this.inte_data = inte_data;
    }
    
    
}
